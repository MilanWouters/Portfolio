// Language Manager
function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang;

    // Update active state of buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });

    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.dataset.i18n;
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'META' && element.getAttribute('name') === 'description') {
                element.setAttribute('content', translations[lang][key]);
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
}

// Mobile Menu Script
document.addEventListener('DOMContentLoaded', () => {
    // Initialize Language
    const savedLang = localStorage.getItem('language') || 'en';
    setLanguage(savedLang);

    // Language Switcher Events
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });

    const navbar = document.querySelector('.navbar');
    const navLinks = document.querySelector('.nav-links');

    // Create Hamburger
    // Check if it already exists to prevent duplication
    let hamburger = document.querySelector('.hamburger');

    if (!hamburger) {
        hamburger = document.createElement('div');
        hamburger.className = 'hamburger';
        hamburger.innerHTML = `
            <span></span>
            <span></span>
            <span></span>
        `;

        // Insert before the right-side container (language + cta)
        // The structure is: .navbar > .nav-logo, .nav-links, div(lang+cta)
        // We want the hamburger to be visible on mobile, likely replacing nav-links or being part of the right side.
        // Let's Append it to the navbar, and CSS will handle positioning.
        navbar.appendChild(hamburger);
    }

    // Toggle Logic
    hamburger.addEventListener('click', () => {
        hamburger.classList.toggle('active');
        navLinks.classList.toggle('active');
        document.body.style.overflow = navLinks.classList.contains('active') ? 'hidden' : '';
    });

    // Close menu when a link is clicked
    document.querySelectorAll('.nav-links a').forEach(link => {
        link.addEventListener('click', () => {
            hamburger.classList.remove('active');
            navLinks.classList.remove('active');
            document.body.style.overflow = '';
        });
    });
});
